      <h2>Muslim Women</h2>
      <div>
        Muslims in British Columbia can choose to be part of a thriving
        community life depending on where they live, which mosques they frequent
        and their own personal choices.

        <br /><br />

        Studies have shown us repeatedly that there is no one typical experience
        for Muslim women across British Columbia. In fact, Muslim women in
        Canada sometimes favour a more “secular” lifestyle (even though their
        skin colour or other markers of identity may prevent this – see section
        on Race and Muslim Identity below). Others want to delve further into
        their identity and culture and there is of course disagreement on the
        “right” way to take these positions as the condition of being human can
        never be uniform.<a href=""><sup>[19]</sup></a>

        <br /><br />

        Women have also actively participated in Muslim associations, although
        it is important to note that they have sometimes had to fight for their
        right to this space. For instance, the Surrey mosque planned by the BCMA
        in 1998 did not have much provision for activities for women and
        children. The design was altered following women’s protests and
        petitions demanding a change.<a href=""><sup>[20]</sup></a> It is also important to note that the
        Muslim sects that have experienced historical discrimination, such as
        the Ahmadi Muslims have been excluded from participation in some Muslim
        associations and spaces and therefore Ahmadi Muslim women would have
        less spaces available to them to congregate.

        <br /><br />

        Similarly, younger members of the Muslim community in BC have not always
        agreed with the views of associations choosing to define themselves and
        their relationship to politics differently. In 2004 this produced a
        controversial situation for Itrath Syed, the Muslim candidate for the
        NDP contesting Delta-East Richmond who refused to backdown on her
        position supporting same sex marriage and, as a result, was publicly
        censured by the imam of the Richmond Mosque.

        <br /><br />

        Following this, the most precariously placed groups, may not have the
        same experiences. For instance, research based on interviews with Muslim
        women fleeing domestic violence in Vancouver, BC have found that women
        tend to prioritize finding a home, but give less priority to building
        community and, even when they find safety, feel isolated and like they
        lack community. This points toward the need for having more avenues and
        outlets where people who may be newcomers or find themselves looking for
        new community connections, can build community and connections
        especially Muslim women who are single parents or fleeing domestic
        violence.<a href=""><sup>[21]</sup></a>

        <br /><br />

        It is Muslim men who tend to figure most prominently in accounts of
        racial profiling, Muslim women tend to be seen as an oppressed group in
        need of saving. For instance in an interview conducted by a graduate
        student at UBC of a young Pakistani-Canadian woman who wore a hijab, she
        described an experience she had at school when Kabul, Afghanistan was
        taken over by the US and its allies:

        <br /><br />

        “I remember I was in grade 9 after the coalition forces had taken Kabul,
        I am in the hallway at my locker and this substitute teacher turns to me
        and touches my shoulder and she says: ‘you are free!’ and I was like
        ‘what?!’ and she’s like ‘you are free now, we took Kabul, you are free!’
        and I was like ‘huh?! I was free before!’ (laughs) like what are you
        talking about? But basically what she was saying was like you don’t have
        to wear that anymore because now we have liberated you over there,
        right? So it’s like there is this constant connection, I have nothing to
        do with Afghanistan but she is connecting it. She has picked up my body
        and placed me in that space.” <a href=""><sup>[22]</sup></a>

        <br /><br />

        Faced with situations like these, women have recounted how things like
        community associations, spaces in mosques and the help of neighbours and
        friends has been uplifting. Other Muslim women have spoken about how
        their experiences after 9/11, have meant that the Muslim community began
        to spend more time with one another and gather in community more so as
        to be a source of support for one another.
      </div>
